//Cycles

/**
 * While(condition){
 * 
 * }
 */

var con=0;
while(con<100){
    console.log(con);
    con++;
}

// Do - While
/**
 * do{
 * 
 * }while(condition);
 */
var con=0;
do{
    console.log(con);
    con++;
}while(con<=5);

// For

/**
 * for(start;condition;update){
 * 
 * }
 */

for(var con=0;con<5;con++){
    console.log(con);
}

